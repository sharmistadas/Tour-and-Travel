export const getDestinationsByType = (type) => {
    return destinations.filter(dest => dest.type === type);
};

export const getDestinationById = (id) => {
    return destinations.find(dest => dest.id === parseInt(id));
};

export const getGroupedDestinations = (type) => {
    const filtered = getDestinationsByType(type);
    const sections = [];

    for (let i = 0; i < filtered.length; i += 3) {
        sections.push(filtered.slice(i, i + 3));
    }

    return sections;
};

